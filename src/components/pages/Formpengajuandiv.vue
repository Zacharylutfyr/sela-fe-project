            pengajuan: [],
            Namais: ''
        }
    },
    methods: {
        async GetApi() {
            await axios
                .get(baseUrl)
                .then((resp) => {
                    this.pengajuan = resp.data;
                })
                .catch((err) => {
                    console.log(err);
                });
        },
        async PostApi() {
            await axios
                .post(baseUrl, {
                    Memo: this.Memois,
                    NIK: this.NIKis,
                    Nama: this.Namais,
                    Divisi: this.Divisiis,
                    Tanggal: this.Tanggalis,
                    Status: this.Statusis,

                })
                .then((resp) => {
                    console.log(resp);
                    this.Namais = '',
                        this.GetApi();
                })
                .catch((err) => {
                    console.log(err);
                });
        },
        async DeleteApi(id) {
            await axios
                .delete(baseUrl + id)
                .then((resp) => {
                    console.log(resp);
                    this.GetApi();
                })
                .catch((err) => {
                    console.log(err);
                });
        }
    },
    mounted() {
        this.GetApi();
    }
}
